    api_endpoints,
    helpers
}
    from '@/utils/hooks'
export default {
    name: 'AnimalObservation',
    props: {
        animal_observation: {
            type: Object,
            required: true
        },
        is_report: {
            type: Boolean,
            required: true
        },
        occurrence_id: {
            type: Number,
            required: true
        },
        processing_status: {
            type: String,
            required: false
        },
        // this prop is only send from split species form to make the original species readonly
        is_readonly: {
            type: Boolean,
            default: false
        },
        is_external: {
            type: Boolean,
            default: false
        },
        isReadOnly: {
            type: Boolean,
            default: false
        },
    },
    data: function () {
        let vm = this;
        return {
            //----list of values dictionary
            primary_detection_method_list: [],
            secondary_sign_list: [],
            reprod_state_list: [],
            death_reason_list: [],
            animal_health_list: [],
            updatingAnimalOnservationDetails: false,
            total_seen: 0,
            listOfAnimalValuesDict: {},
            not_counted: false,
        }
    },
    components: {
        FormSection,
    },
    watch: {
        animal_observation: function () {
            let vm = this;
            $(vm.$refs.primary_detection_select).val(vm.animal_observation.primary_detection_method).trigger('change.select2');
            //$(vm.$refs.secondary_sign_select).val(vm.animal_observation.secondary_sign).trigger('change.select2');
            $(vm.$refs.reproductive_state_select).val(vm.animal_observation.reproductive_state).trigger('change.select2');
        }
    },
    methods: {
        calculateTotalNumberSeen: function () {
            let vm = this;
            vm.total_seen = 0;
            $('input.animal-count-input').each(function (i, n) {
                vm.total_seen += parseInt($(n).val(), 10);
            });
            return vm.total_seen;
        },
        checkboxChanged: function () {
            this.not_counted = this.animal_observation.counted == 'false';
        },
        initialisePrimaryDetectionSelect: function () {
            let vm = this;
            // Initialise select2 for proposed Conservation Criteria
            $(vm.$refs.primary_detection_select).select2({
                "theme": "bootstrap-5",
                allowClear: true,
                multiple: true,
                placeholder: "Select Primary Detection Method",
            }).
                on("select2:select", function (e) {
                    var selected = $(e.currentTarget);
                    vm.animal_observation.primary_detection_method = selected.val();
                }).
                on("select2:unselect", function (e) {
                    var selected = $(e.currentTarget);
                    vm.animal_observation.primary_detection_method = selected.val();
                });
        },
        initialiseSecondarySignSelect: function () {
            let vm = this;
            // Initialise select2 for proposed Conservation Criteria
            $(vm.$refs.secondary_sign_select).select2({
                "theme": "bootstrap-5",
                allowClear: true,
                multiple: true,
                placeholder: "Select Secondary Signs",
            }).
                on("select2:select", function (e) {
                    var selected = $(e.currentTarget);
                    vm.animal_observation.secondary_sign = selected.val();
                }).
                on("select2:unselect", function (e) {
                    var selected = $(e.currentTarget);
                    vm.animal_observation.secondary_sign = selected.val();
                });
        },
        initialiseReprodStateSelect: function () {
            let vm = this;
            // Initialise select2 for proposed Conservation Criteria
            $(vm.$refs.reproductive_state_select).select2({
                "theme": "bootstrap-5",
                allowClear: true,
                multiple: true,
                placeholder: "Select Reproductive State",
            }).
                on("select2:select", function (e) {
                    var selected = $(e.currentTarget);
                    vm.animal_observation.reproductive_state = selected.val();
                }).
                on("select2:unselect", function (e) {
                    var selected = $(e.currentTarget);
                    vm.animal_observation.reproductive_state = selected.val();
                });
        },
        updateAnimalObservationDetails: function () {
            let vm = this;
            vm.updatingAnimalOnservationDetails = true;
            let endpoint = api_endpoints.occurrence;
            if (vm.is_report) {
                endpoint = api_endpoints.occurrence_report;
            }
            vm.$http.post(helpers.add_endpoint_json(endpoint, (vm.occurrence_id + '/update_animal_observation_details')), JSON.stringify(vm.animal_observation), {
                emulateJSON: true
            }).then((response) => {
                vm.updatingAnimalOnservationDetails = false;
                //vm.animal_observation = response.body;
                swal.fire({
                    title: 'Saved',
                    text: 'Animal Observation details have been saved',
                    icon: 'success',
                    customClass: {
                        confirmButton: 'btn btn-primary'
                    },
                }).then((result) => {
                    if (vm.processing_status == "Unlocked") {
                        vm.$router.go();
                    }
                });
            }, (error) => {
                var text = helpers.apiVueResourceError(error);
                swal.fire({
                    title: 'Error',
                    text: 'Animal Observation details cannot be saved because of the following error: ' + text,
                    icon: 'error',
                    customClass: {
                        confirmButton: 'btn btn-primary'
                    },
                });
                vm.updatingAnimalOnservationDetails = false;
            });
        },
    },
    created: async function () {
        let vm = this;
        const res = await Vue.http.get(`/api/occurrence/animal_observation_list_of_values.json`);
        vm.listOfAnimalValuesDict = res.body;
        vm.primary_detection_method_list = vm.listOfAnimalValuesDict.primary_detection_method_list;
        vm.primary_detection_method_list.splice(0, 0,
            {
                id: '',
                name: '',
            });
        vm.secondary_sign_list = vm.listOfAnimalValuesDict.secondary_sign_list;
        vm.secondary_sign_list.splice(0, 0,
            {
                id: '',
                name: '',
            });
        vm.reprod_state_list = vm.listOfAnimalValuesDict.reprod_state_list;
        vm.reprod_state_list.splice(0, 0,
            {
                id: '',
                name: '',
            });
        vm.death_reason_list = vm.listOfAnimalValuesDict.death_reason_list;
        vm.death_reason_list.splice(0, 0,
            {
                id: null,
                name: null,
            });
        vm.animal_health_list = vm.listOfAnimalValuesDict.animal_health_list;
        vm.animal_health_list.splice(0, 0,
            {
                id: null,
                name: null,
            });
    },
    mounted: function () {
        let vm = this;
        vm.initialisePrimaryDetectionSelect();
        vm.initialiseSecondarySignSelect();
        vm.initialiseReprodStateSelect();
        vm.calculateTotalNumberSeen();
        vm.not_counted = !vm.animal_observation.counted;
    },
}
</script>

